
# Syntax of While loop

# startNum                    //step1
# while condition:            //step2
#     while loop body
#     incr/decr               //step3


print("----print num from 1 to 5-----")

num=1
#      6<6
while num<6:
    print(num)      #1 2 3 4 5
    num+=1          #6      # num=num+1

print("-------")

num=1
#      6<=5
while num<=5:
    print(num)           #1 2 3 4 5
    num+=1               #6

print("----print num from 100 to 150-----")
starNum=100
while starNum<=150:
    print(starNum)
    starNum+=1


print("----print even num from 2 to 8-----")
starNum=2
while starNum<=8:
    print(starNum)
    starNum+=2


print("----print num from 5 to 1-----")
num=5
#          0>=1
while num>=1:
    print(num)        #5 4 3 2 1
    num-=1            #0


